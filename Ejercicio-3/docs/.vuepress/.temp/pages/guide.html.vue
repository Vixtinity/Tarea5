<template><div><h1 id="guia-de-uso" tabindex="-1"><a class="header-anchor" href="#guia-de-uso"><span>Guía de Uso</span></a></h1>
<h2 id="como-hacer-una-reserva" tabindex="-1"><a class="header-anchor" href="#como-hacer-una-reserva"><span>Cómo hacer una reserva</span></a></h2>
<ol>
<li>
<p><strong>Selecciona una Hora</strong>:
En la parte superior, haz clic en una de las horas disponibles (ej: 13:00, 14:00).
El mapa se actualizará para mostrar el estado de las mesas en esa hora.</p>
</li>
<li>
<p><strong>Selecciona una Mesa</strong>:
Busca una mesa Libre (color verde).
Haz clic en ella. Se marcará en azul como seleccionada.
<em>Nota: No puedes seleccionar mesas Ocupadas (rojo).</em></p>
</li>
<li>
<p><strong>Completa el Formulario</strong>:
Aparecerá un formulario a la derecha (o debajo en móviles).
Ingresa:</p>
<ul>
<li><strong>Nombre</strong>: Obligatorio.</li>
<li><strong>Email</strong>: Debe ser válido.</li>
<li><strong>Comensales</strong>: Número de personas (no debe superar la capacidad de la mesa).</li>
</ul>
</li>
<li>
<p><strong>Confirmar</strong>:
Haz clic en &quot;Confirmar Reserva&quot;.
Si todo es correcto, recibirás una alerta de confirmación y la mesa pasará a estar ocupada.</p>
</li>
</ol>
<h2 id="accesibilidad" tabindex="-1"><a class="header-anchor" href="#accesibilidad"><span>Accesibilidad</span></a></h2>
<ul>
<li>Puedes usar la tecla <code v-pre>TAB</code> para navegar por los horarios y mesas.</li>
<li>Usa <code v-pre>ENTER</code> para seleccionar.</li>
<li>Los lectores de pantalla anunciarán el estado y capacidad de cada mesa.</li>
</ul>
</div></template>


